    private void loadReceptionistId() {
        try
        {
            List<String>receptionistName=ReceptionistDAO.getAllReceptionistUserName();
           if(receptionistName.isEmpty())
           {
               JOptionPane.showMessageDialog(null,"No receptionist present!");
           return;
           }
           btnRemove.setEnabled(true);
            for(String userName:receptionistName)
            {
                jcReceptionistId.addItem(userName);
            }
        }
         catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"DB ERrror!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
    }
