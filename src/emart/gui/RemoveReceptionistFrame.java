    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
    String userName=jcReceptionistId.getSelectedItem().toString();
         try
         {
           boolean result=ReceptionistDAO.deleteReceptionist(userName);
           if(result)
           {
               JOptionPane.showMessageDialog(null,"Receptionist deleted successfully!");
               jcReceptionistId.removeItemAt(jcReceptionistId.getSelectedIndex());
               if(jcReceptionistId.getItemCount()==0)
                   btnRemove.setEnabled(false);
               return;
           }
           JOptionPane.showMessageDialog(null,"Receptionist not deleted!");
         }
         catch(SQLException ex)
         {
           JOptionPane.showMessageDialog(null,"DB ERrror!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
         }
    }//GEN-LAST:event_btnRemoveActionPerformed

