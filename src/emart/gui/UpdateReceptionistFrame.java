    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs())
            return;
        try
        {
            String userid=jcReceptionistId.getSelectedItem().toString();
            boolean result=ReceptionistDAO.updatePassword(userid,PasswordEncryption.getEncryptedPassword(password));
            if(result)
            {
              JOptionPane.showMessageDialog(null,"Password changed successfully!");  
              clearText();
              jcReceptionistId.setSelectedIndex(0);
              return;
            }
            JOptionPane.showMessageDialog(null,"Password not changed!");
            clearText();
         }
           catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"DB ERrror!","Error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

